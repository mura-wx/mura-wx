# operasi aritmatika

x = 10
y = 9

# pertambahan +
print("CONTOH PERTAMBAHAN +")
hasil = x + y
print (x,"+",y,"=",hasil)

# pengurangan -
print("CONTOH PEGURANGAN -")
hasil = x - y
print (x,"-",y,"=",hasil)

# perkalian *
print("CONTOH PERKALIAN *")
hasil = x * y
print (x,"*",y,"=",hasil)

# pembagian /
print("CONTOH PEMBGIAN /")
hasil = x / y
print (x,"/",y,"=",hasil)

# eksponen (pangkat) **
print("CONTOH EKSPONEN (PANGKAT) **")
hasil = x ** y
print (x,"**",y,"=",hasil)

# floor division (pembagian dengan hasil di bulatkan kebawah) **
print("CONTOH FLOOR DIVISION //")
hasil = x // y
print (x,"//",y,"=",hasil)
