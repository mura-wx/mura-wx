print("\nPROGRAM BILANGAN TEMPETURE\n")

# celcius
celcius = int(input("masukan suhu dalam celcius:" ))
print ("Celcius:", celcius, "C")

# fahrenheit
fahrenheit = ((9/5) * celcius) + 32
print ("Fahrenheit:", fahrenheit, "F")

# reamur
reamur = (4/5) * celcius
print ("Reamur:", reamur, "R")

# kelvin
kelvin = celcius + 237
print ("Kelvin:", kelvin, "K")
